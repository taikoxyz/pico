#!/usr/bin/env node
import { readFileSync, writeFileSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const HERE = dirname(fileURLToPath(import.meta.url));
const CLI_ROOT = resolve(HERE, '..');
const REPO_ROOT = resolve(CLI_ROOT, '..', '..');

const SOURCES = {
  CLI_VERSION: resolve(CLI_ROOT, 'package.json'),
  SDK_VERSION: resolve(REPO_ROOT, 'packages/sdk/package.json'),
  PROTOCOL_VERSION: resolve(REPO_ROOT, 'packages/protocol/package.json'),
};

const versions = Object.fromEntries(
  Object.entries(SOURCES).map(([key, path]) => [
    key,
    JSON.parse(readFileSync(path, 'utf8')).version,
  ]),
);

const out = resolve(CLI_ROOT, 'src/generated/versions.ts');
const body = [
  '// Auto-generated by apps/cli/scripts/generate-versions.mjs. Do not edit by hand.',
  '// Source: apps/cli/package.json, packages/sdk/package.json, packages/protocol/package.json.',
  '',
  ...Object.entries(versions).map(([k, v]) => `export const ${k} = '${v}';`),
  '',
].join('\n');

writeFileSync(out, body);
console.info(`wrote ${out}`);
for (const [k, v] of Object.entries(versions)) console.info(`  ${k} = ${v}`);
